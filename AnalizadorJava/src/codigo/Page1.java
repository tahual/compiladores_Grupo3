    private void btnAnalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnalizarActionPerformed

        //Se crea un archivo temporal para la escritura y lectura de ambos textbox
        File archivoResultado = new File("archivo.txt");
        //cada lectura y escritura debe de ir acompa√±ado de un try catch
        PrintWriter escribir;
        try {
            escribir = new PrintWriter(archivoResultado);
            escribir.print(txtEntrada.getText());
            escribir.close();

        } catch (FileNotFoundException ex) {
            Logger.getLogger(Page1.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            Reader leer = new BufferedReader(new FileReader("archivo.txt"));
            Lexer lexer = new Lexer(leer);

            String resultado = "";

            while (true) {

                Tokens tokens = lexer.yylex();
                //si es la ultima linea del token, realiza la escritura final
                if (tokens == null){
                    resultado += "FIN";
                    txtResultado.setText(resultado);
                    return;
                }

                switch (tokens) {
                    //se valida segun el tipo de token, para retornar una respuesta (aqui esta de forma muy general)
                    case ERROR:
                    resultado += "NO DEFIINDO\n";
                    break;
                    case INICIO:
                    break;
                    case Identificador: case Numero: case Reservadas:
                    resultado += lexer.lexeme + ": Es " + tokens + "\n";
                    break;
                    default:
                    resultado +=  lexer.lexeme + " Token " + tokens + "\n";
                    break;
                }

            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Page1.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Page1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAnalizarActionPerformed


